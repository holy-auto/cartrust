-- ============================================================
-- 車種サイズマスタ + vehiclesテーブルへのsize_class追加
-- 体積基準（全長×全幅×全高 = ㎥）でサイズ分類
-- ============================================================

-- 1. vehiclesテーブルにsize_classカラム追加
ALTER TABLE vehicles ADD COLUMN IF NOT EXISTS size_class text
  CHECK (size_class IS NULL OR size_class IN ('SS', 'S', 'M', 'L', 'LL', 'XL'));

-- 2. 車種サイズマスタテーブル
CREATE TABLE IF NOT EXISTS vehicle_size_master (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  maker text NOT NULL,
  model text NOT NULL,
  size_class text NOT NULL CHECK (size_class IN ('SS', 'S', 'M', 'L', 'LL', 'XL')),
  body_type text,
  full_length_mm integer,
  full_width_mm integer,
  full_height_mm integer,
  volume_m3 numeric(5,2) GENERATED ALWAYS AS (
    CASE WHEN full_length_mm IS NOT NULL AND full_width_mm IS NOT NULL AND full_height_mm IS NOT NULL
    THEN ROUND((full_length_mm::numeric * full_width_mm::numeric * full_height_mm::numeric) / 1000000000, 2)
    ELSE NULL END
  ) STORED,
  notes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE(maker, model)
);

-- RLS
ALTER TABLE vehicle_size_master ENABLE ROW LEVEL SECURITY;

CREATE POLICY "vehicle_size_master_select" ON vehicle_size_master
  FOR SELECT USING (true);

CREATE POLICY "vehicle_size_master_admin_all" ON vehicle_size_master
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM tenant_memberships tm
      WHERE tm.user_id = auth.uid()
        AND tm.role IN ('owner', 'admin')
    )
  );

-- ============================================================
-- サイズ分類基準（体積ベース: 全長×全幅×全高）
-- SS:  8.0㎥ 未満      （軽自動車: アルト, ミラ, コペン等）
-- S:   8.0～10.0㎥     （コンパクト: アクア, フィット, ノート等）
-- M:  10.0～12.0㎥     （セダン/中型: プリウス, シビック等）
-- L:  12.0～14.0㎥     （SUV/ワゴン: クラウン, アルファード等）
-- LL: 14.0～16.0㎥     （大型: ランクル, ハイエース等）
-- XL: 16.0㎥ 超        （フルサイズ: ランクル300, レンジローバー等）
-- ============================================================

-- 3. 体積から自動でsize_classを判定する関数
CREATE OR REPLACE FUNCTION calc_size_class_from_volume(vol_m3 numeric)
RETURNS text
LANGUAGE sql IMMUTABLE
AS $$
  SELECT CASE
    WHEN vol_m3 IS NULL THEN NULL
    WHEN vol_m3 < 8.0  THEN 'SS'
    WHEN vol_m3 < 10.0 THEN 'S'
    WHEN vol_m3 < 12.0 THEN 'M'
    WHEN vol_m3 < 14.0 THEN 'L'
    WHEN vol_m3 < 16.0 THEN 'LL'
    ELSE 'XL'
  END;
$$;

-- 4. メーカー/車種から判定する関数
CREATE OR REPLACE FUNCTION estimate_vehicle_size(p_maker text, p_model text)
RETURNS text
LANGUAGE plpgsql STABLE
AS $$
DECLARE
  v_size text;
BEGIN
  SELECT size_class INTO v_size
  FROM vehicle_size_master
  WHERE maker = p_maker AND model = p_model
  LIMIT 1;
  IF v_size IS NOT NULL THEN RETURN v_size; END IF;

  SELECT size_class INTO v_size
  FROM vehicle_size_master
  WHERE maker = p_maker AND (
    p_model ILIKE '%' || model || '%'
    OR model ILIKE '%' || p_model || '%'
  )
  LIMIT 1;
  RETURN v_size;
END;
$$;

